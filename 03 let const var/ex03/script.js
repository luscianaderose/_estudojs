// 3. Crie um programa que receba três notas de prova de um aluno digitadas pelo
// usuário, calcule a média aritmética dessas notas e imprima a média.

const n1 = parseFloat(prompt("Digite a nota 1."))
const n2 = parseFloat(prompt("Digite a nota 2."))
const n3 = parseFloat(prompt("Digite a nota 3."))

alert("A média é " + ((n1 + n2 + n3) / 3))