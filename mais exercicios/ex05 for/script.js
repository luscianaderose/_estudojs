// Objetivo: Criar uma função que recebe uma frase e um número, 
// e imprime a frase repetida esse número de vezes.

function repetirFrase(frase, vezes) {
    for(let i = 0; i < vezes; i++){
        console.log(frase)
    }
}

repetirFrase("Olá mundo!", 3);
// Saída esperada:
// Olá mundo!
// Olá mundo!
// Olá mundo!