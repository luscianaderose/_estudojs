// 7. Crie um programa que calcule o resto da divisão 
// de dois números inteiros digitados pelo usuário no console.

var n1 = parseInt(prompt("Digite um número inteiro."))
var n2 = parseInt(prompt("Digite outro número inteiro."))
var resto = n1 % n2
alert("O resto da divisão de " + n1 + " e " + n2 + " é " + resto + ".")
