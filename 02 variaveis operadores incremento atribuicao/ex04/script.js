// 4. Crie um programa que recebe dois números inteiros 
// digitados pelo usuário e exiba o resultado 
// da multiplicação desses números.

var n1 = parseInt(prompt("Digite um número."))
var n2 = parseInt(prompt("Digite outro número."))
var resultado = n1 * n2
alert(n1 + " x " + n2 + " = " + resultado)
